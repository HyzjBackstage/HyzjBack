package hyzj.demo.Model;

public class IncomeYear {
    private String income_year_id;
    private String time;
    private String amount_year;

    public String getIncome_year_id() {
        return income_year_id;
    }

    public void setIncome_year_id(String income_year_id) {
        this.income_year_id = income_year_id;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getAmount_year() {
        return amount_year;
    }

    public void setAmount_year(String amount_year) {
        this.amount_year = amount_year;
    }

}
