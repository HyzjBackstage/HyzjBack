package hyzj.demo.Model;

public class IncomeMonth {
    private String income_month_id;
    private String time;
    private String amount_month;

    public String getIncome_month_id() {
        return income_month_id;
    }

    public void setIncome_month_id(String income_month_id) {
        this.income_month_id = income_month_id;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getAmount_month() {
        return amount_month;
    }

    public void setAmount_month(String amount_month) {
        this.amount_month = amount_month;
    }
}
