package hyzj.demo.Model;

public class IncomeDay {
    private String income_day_id;
    private String time;
    private String amountMonth;

    public String getIncome_day_id() {
        return income_day_id;
    }

    public void setIncome_day_id(String income_day_id) {
        this.income_day_id = income_day_id;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getAmountMonth() {
        return amountMonth;
    }

    public void setAmountMonth(String amountMonth) {
        this.amountMonth = amountMonth;
    }
}
